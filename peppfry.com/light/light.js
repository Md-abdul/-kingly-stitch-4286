let main_Container = document.querySelector(".prodcant");
let cartArr = JSON.parse(localStorage.getItem("cart")) || [];
let countp = 0;
function display(lights) {
  main_Container.innerHTML = "";
  lights.forEach((light) => {
    let card = document.createElement("div");
    let img = document.createElement("img");
    let details = document.createElement("h3");
    let price = document.createElement("h4");
    let discount = document.createElement("p");
    let button = document.createElement("button");

    img.src = light.img;
    details.innerText = light.details;
    price.innerText = `₹ ${light.price}`; //`₹${product.price}`
    discount.innerText = light.discount;
    button.innerText = "Add to cart";
    button.setAttribute("class", "card-btn");
    button.addEventListener("click", () => {
      cartArr.push({ ...light, quantity: 1 });
      localStorage.setItem("cart", JSON.stringify(cartArr));
      alert("Product Added to cart");

      countp++;
      localStorage.setItem("totpro", countp);
    });

    card.append(img, details, price, discount, button);
    main_Container.append(card);
  });
}
display(lights);

let sorting = document.getElementById("pricesort");
sorting.addEventListener("change", (e) => {
  let sortingdata = e.target.value;
  console.log(sortingdata);
  if (sortingdata === "lth") {
    let x = [...lights];
    x.sort((a, b) => a.price - b.price);
    console.log(x);
    display(x);
  } else {
    let y = [...lights];
    y.sort((a, b) => b.price - a.price);
    console.log(y);
    display(y);
  }
});

